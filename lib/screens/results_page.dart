  final String interpretation;
  final String bmi;

  ResultsPage(
      {@required this.result,
      @required this.interpretation,
      @required this.bmi});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('BMI CALCULATOR'),
